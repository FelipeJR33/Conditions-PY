# quantidade de água ingerida em litro.
quantidadeDeAguaIngerida = 10

if quantidadeDeAguaIngerida >= 3:
    print("Risco Baixo - Você está ingerindo uma boa quantidade de água, parabéns!")
elif quantidadeDeAguaIngerida < 1.5:
    print("Risco Alto - Você está ingerindo pouquissima água, beba mais água!")
else:
    print("Risco Moderado - Você está ingerindo pouca água, beba mais!")
